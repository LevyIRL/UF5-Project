package model;

import java.io.Serializable;

/**
 *
 * @author levy
 */
public class Region implements Serializable{
    private String name;

    public Region(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Region{" + "name=" + name + '}';
    }

    
}
